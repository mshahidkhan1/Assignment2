function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
var firstLocation = city_country("Karachi", "Pakistan");
var secondLocation = city_country("London", "United Kingdom");
var thirdLocation = city_country("Sydney", "Australia");
console.log(firstLocation);
console.log(secondLocation);
console.log(thirdLocation);
